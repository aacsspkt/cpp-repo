#include <iostream>

int main() {
    /*
    constant can be created using 'const' keyword in left side of variable declared.
    Using 'const' will declared variable readonly and compile with check that it is
    unchanged. It helps in data security.
    */

    const double PI = 3.14159;
    const int LIGHT_SPEED = 299792458;
    const int WIDTH = 1920;
    const int HEIGHT = 1080;


    return 0;
}